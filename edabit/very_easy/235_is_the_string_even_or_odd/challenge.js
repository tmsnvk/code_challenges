// Given a string, return true if its length is even or false if the length is odd.

// Examples
// oddOrEven("apples") ➞ true

// oddOrEven("pears") ➞ false

// oddOrEven("cherry") ➞ true

const oddOrEven = (s) => s.length % 2 === 0 ? true : false;