// String to Integer and Vice Versa
// Write two functions:

// toInt() : A function to convert a string to an integer.
// toStr() : A function to convert an integer to a string.

function toInt(str) {
	const x = parseInt(str);
	return x;
}

function toStr(int) {
	return int.toString();
}
